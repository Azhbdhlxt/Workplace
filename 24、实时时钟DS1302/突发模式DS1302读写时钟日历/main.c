/*=================================================
*函数名称：DS1302_Clear_WP
*函数功能：清除DS1302写保护
*说明：先写入命令0x8e（写控制寄存器）接着向该寄存器写0
*调用：DS1302_W_DAT()
=================================================*/
void DS1302_Clear_WP()
{
	DS1302_W_DAT(0x8e,0x00);  //把控制寄存器WP位置0
}
/*=================================================
*函数名称：DS1302_Clear_WP
*函数功能：设置DS1302写保护
*说明：先写入命令0x8e（写控制寄存器）接着向该寄存器写0x80
*调用：DS1302_W_DAT()
=================================================*/
void DS1302_Set_WP()
{	
	DS1302_W_DAT(0x8e,0x80); //把控制寄存器WP位置1
	TRST = 0;				 //拉低使能端
	TSCLK = 0;				 //拉低数据总线
} 
/*=================================================
*函数名称：Set_DS1302_Time
*函数功能：设置DS1302时钟日历数据
*说明：把时钟日历暂存数组TimeData数据转换为BCD码并
	   写入到DS1302时钟日历寄存器中
*调用：DS1302_Clear_WP();DS1302_W_Byte();DS1302_Set_WP();
*输入：写时钟突发模式寄存器命令DS1302_W_ADDR
=================================================*/
void Set_DS1302_Time()
{
	uchar i, j;
	DS1302_Clear_WP();		//清除写保护
	TRST = 0;			 	//拉低使能端
	TSCLK = 0;				//拉低数据总线
	TRST = 1;				//拉高使能端，开始写数据
	DS1302_W_Byte(DS1302_W_ADDR); //写时钟突发模式寄存器
	for(i = 0; i < 7; i++)	//写入7个字节的时钟初始值
	{
		j = TimeData[i]/10;	 //BCD码转换
		TimeData[i] %= 10;	 //BCD码转换
		TimeData[i] += j*16; //BCD码转换
		DS1302_W_Byte(TimeData[i]); //在突发模式下可以连续写数据
	}
	DS1302_Set_WP(); //开起写保护		
}
/*=================================================
*函数名称：Read_DS1302_Time
*函数功能：读取DS1302时钟数据
*说明：	读取DS1302时钟数据 返回数据存入时钟日历暂存
		数组TimeData（数据格式BCD码）
*调用：DS1302_Clear_WP();DS1302_R_Byte();DS1302_Set_WP();
*输入：写 读时钟突发模式寄存器命令DS1302_R_ADDR
=================================================*/
void Read_DS1302_Time()
{
	uchar i;
	DS1302_Clear_WP();    	//清楚些保护
	TRST = 0;			 	//拉低使能端
	TSCLK = 0;				//拉低数据总线
	TRST = 1;				//拉高使能端，开始写数据
	DS1302_W_Byte(DS1302_R_ADDR); //写 读突发模式寄存器命令
	for(i = 0; i < 7; i++)	//从DS1302读取7个字节的时钟日历数据
	{
		TimeData[i] = DS1302_R_Byte();//在突发模式下可以连续读数据
	}
	DS1302_Set_WP();   //开起写保护
} 
/*=================================================
*函数名称：main
*函数功能：主函数 1602液晶显示DS1302数据
		   Date:年-月-日-星期
		   Time:时：分：秒
=================================================*/
void main()
{
	InitLcd1602();              	//1602初始化
	Set_DS1302_Time(); //时钟突发模式写
	LcdShowStr(0,0,"Date:");  //1602显示字符串
	LcdShowStr(0,1,"Time:");  //1602显示字符串
	while(1)
	{
		Read_DS1302_Time();//时钟突发模式读 
		DisplayOneStr (5,0,TimeData[6]); //年
		DisplayOneChar (7,0,'-');		 //-
		DisplayOneStr (8,0,TimeData[4]); //月
		DisplayOneChar (10,0,'-');		 //-
		DisplayOneStr (11,0,TimeData[3]);//日
		DisplayOneChar (13,0,'-');		 //-
		DisplayOneStr (14,0,TimeData[5]);//星期

		DisplayOneStr (6,1,TimeData[2]); //时
		DisplayOneChar (8,1,':');		 //：
		DisplayOneStr (9,1,TimeData[1]); //分
		DisplayOneChar (11,1,':');		 //：
		DisplayOneStr (12,1,TimeData[0]);//秒
		Delay_Ms(1000);					 //延时1秒
	}		
}
